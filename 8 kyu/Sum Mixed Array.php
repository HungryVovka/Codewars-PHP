// -----------------------------------------------------------
// Given an array of integers as strings and numbers, return the sum of the array values as if all were numbers.
// 
// Return your answer as a number.
// -----------------------------------------------------------

function sum_mix($a){
	return array_reduce($a, function($i, $j){
		return intval($i) + intval($j);
	}, 0);
}

